    void loadDataInTable() {
        List<User> allUsers = new UserDaoImpl().getAllUsers();

//         allUsers.forEach((user)->System.out.println(user));
//        for (User user : allUsers) 
//        {
//            System.out.println(user);
//        }
        DefaultTableModel dtm = new DefaultTableModel(new String[]{"Id", "Name", "Salary", "Phone No."}, 0);
        for (int i = 0; i < allUsers.size(); i++) {
            User user = allUsers.get(i);
//            System.out.println(user);
            Object[] data = {user.getId(), user.getName(), user.getSalary(), user.getPhoneno()};
            dtm.addRow(data);
        }
        allUsersTable.setModel(dtm);

    }

